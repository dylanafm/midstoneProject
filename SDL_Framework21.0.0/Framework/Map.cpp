Map::Map(SDL_Renderer* renderer, int stage) {

	stageNum = stage;
	water = TextureManager::LoadTexture("textures/water.png", renderer);
	sand = TextureManager::LoadTexture("textures/sand.png", renderer);
	coral = TextureManager::LoadTexture("textures/coral.png", renderer);

	if( water == nullptr){
		printf("Yo Water not loadin\n");
	}
	
	if (stageNum == 1) {
		LoadMap(lvl1p1);
	}
	if (stageNum == 2) {
		LoadMap(lvl1p2);
	}
	
	//pos.x = 



	src.x = src.y = 0;
	src.w =  32;
	src.h =  32;

	dest.x = dest.y = 0;

	dest.w = 32;
	dest.h = 32;
}

void Map::LoadMap(int arr[23][40])
{
	for (int row = 0; row < 23; row++)
	{
		for (int column = 0; column < 40; column++)
		{
			map[row][column] = arr[row][column];

		}
	}
}

void Map::UpdateStage(int stage)
{
	stageNum = stage;
	std::cout << stageNum << std::endl;
}

void Map::DrawMap(SDL_Renderer* renderer)
{
	int type = 0;

	for (int row = 0; row < 23; row++)
	{
		for (int column = 0; column < 40; column++)
		{
			type = map[row][column];

			dest.x = column * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(water, src, dest, renderer);
				break;

			case 1:
				TextureManager::Draw(sand, src, dest, renderer);
				break;
			case 2:
				TextureManager::Draw(coral, src, dest, renderer);
				break;

			default:
				break;
			}

		}
	}


}